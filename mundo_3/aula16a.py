lanche = ('Hambúrguer', 'Suco', 'Pizza', 'Pudim') # Tupla

print(lanche)
print(lanche[1]) # começa no 0
print(lanche[3])
print(lanche[-2]) # de trás para frente
print(lanche[1:3]) # do elemento 1 atés ANTES DO elemento 3 (ou seja, 1 e 2)
print(lanche[2:]) # do elemento 2 até o fim
print(lanche[:2]) # do início até ANTES DO elemento 2
